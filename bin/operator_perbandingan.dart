// Operator perbandingan merupakan operator yang hasilnya adalah boolean
// Bisa digunakan di tipe data number dan string.
void main(List<String> args) {
  print(10 == 10);
  print(10 != 10);
  print(10 > 10);
  print(10 < 10);
  print(10 >= 10);
  print(10 <= 10);

  print('faisal' == 'faisal');
  print('faisal' != 'faisal');
}
