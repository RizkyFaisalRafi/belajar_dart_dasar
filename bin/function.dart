/*
Function
Function adalah blok kode program yang akan berjalan saat kita panggil
Untuk membuat function, kita bisa menggunakan kata kunci void lalu diikuti dengan 
nama function, kurung () dan diakhiri dengan block {}

Memanggil function dengan cara memanggil nama function lalu diikuti tanda kurung()
Di bahasa pemrograman laion, function juga disebut Method()
*/

void sayHello() {
  print("Hello World");
}

void main(List<String> args) {
  sayHello(); // memanggil function
  sayHello(); // memanggil function
  sayHello(); // memanggil function
}
