/*
  Operator Aritmatika
  + (Penambahan)
  -, -10 (Pengurangan atau negatif)
  * (Perkalian)
  / (Pembagian yang berupa hasilnya Double)
  ~/ (Pembagian yang berupa hasilnya Integer)
  % (Sisa Bagi / Modulus / modulo)
*/
void main(List<String> args) {
  var first = 10;
  var second = 3;

  print(first + second); // 10 + 3
  print(first - second); // 10 - 3
  print(first * second); // 10 * 3
  print(first / second); // 10 / 3
  print(first ~/ second); // 10 ~/ 3
  print(first % second); // 10 % 3
}
